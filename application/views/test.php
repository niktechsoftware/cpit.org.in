					<h1>Welcome to Markandey ITI</h1>
					<p><img alt="" src="userfiles/images/gaurav-tiwari.jpg" style="float:left; height:156px; margin:0 20px 20px 0; width:200px" />Markandey ITI is a renowned ITI college situated in Varanasi, Chaubeypur market along the Varanasi &ndash; Ghazipur national highway. It was established in the year 2007 after the name of Markandey Mahadev and grandfather (Late Markandey Tiwari) of the founder father Mr. Arun Kumar Tiwari and with advised of his son Mr. Gaurav Tiwari , B.Tech, IIT Kanpur and presently Gaurav Tiwari (IPS) working as a IPS officer. Markandey Industrial Training Centre is approved by NCVT( Reg. No. DGET-6/24/101/2009-TC, college code: 1721) and is giving training of Fitter and Electrician trade. Markandey ITI is well equipped with all the relevant devices and workshop. All the trainers are skilled, experienced and expert of their area. This centre from the very beginning has been giving the best performance in all areas, academic and co-curricular activities. In the year 2010, Government ITI Karaudi organized an inter level sports competition in government ITI Chaukaghat in which 23 ITIs participated but Markandey ITI showing the best performance won the trophy of championship. This competition was inaugurated by then commissioner of Varanasi and the principal of ITI Karaudi and ITI Chaukaghat Mr. M.L. Gupta and Mr. V.K. Prasad respectively distributed the prizes.</p>

<p>Markandey ITI has rich Library, Sports room, Dispensary, Canteen, Wi-Fi campus and Hostel facility.</p>

